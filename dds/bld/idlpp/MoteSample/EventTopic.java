package MoteSample;

public final class EventTopic {

    public java.lang.String name = "";
    public java.lang.String location = "";
    public int time;

    public EventTopic() {
    }

    public EventTopic(
        java.lang.String _name,
        java.lang.String _location,
        int _time)
    {
        name = _name;
        location = _location;
        time = _time;
    }

}
